#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);

    int n;
    cin >> n;

    int Z[n];
    for (int i = 0; i < n; i++)
    {
        cin >> Z[i];
    }

    for (int i = 0; i < n; i++)
    {
        if (abs(Z[i] - i) > 1)
        {
            cout << "NO" << endl;
            return 0;
        }
    }

    cout << "YES" << endl;
    return 0;
}