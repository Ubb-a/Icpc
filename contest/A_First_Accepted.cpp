#include <bits/stdc++.h>

using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);

    string a, b;
    cin >> a >> b;

    int LA = a.back() - '0';
    int LB = b.back() - '0';

    int sum = LA + LB;

    if (sum % 2 == 0)
    {
        cout << "Even" << endl;
    }
    else
    {
        cout << "Odd" << endl;
    }

    return 0;
}
