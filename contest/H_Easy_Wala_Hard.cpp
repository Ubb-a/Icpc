#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    string z;
    getline(cin, z);

    int n = z.length();
    bool t = true;

    for (int i = 0; i < n; i++)
    {
        if (z[i] == ' ')
        {
            if (z[i - 1] != z[i + 1])
            {
                t = false;
                break;
            }
        }
    }

    if (t && z[n - 1] == z[0])
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }

    return 0;
}